//2. Write a C program to add two numbers.
#include<stdio.h>
int main()
{
    int a,b,result=0;
    printf("Enter the first number");
    scanf("%d",&a);
    printf("Enter the second number");
    scanf("%d",&b);
    result=a+b;
    printf("The addition of first and second number is %d:\n",result);
    return 0;
}




    


